			<blockquote>As a Catholic institution, Notre Dame University Forms part of the saving mission of the Church, especially for education in faith (Gravissimum Educationis, 3). To fulfill this mission the University founds its whole educational enterprise in Christ and His Word (Congregation for Catholic Education, the Catholic School) and links itself with the magisterium, the guardian of the faith (Lumen Gentium, 25). With all its distinctive aims and means, the University offers itself in service to all, non-Christians included, acknowledging, preserving and promoting the spiritual and moral goods found among these persons as well as the values in their society and culture (Nostre Aetate, 2).</blockquote>

